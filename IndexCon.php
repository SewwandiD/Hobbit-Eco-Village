        <h1>Location</h1>
        <p align="justify" style="color: #030; font-family:'Arial Black', Gadget, sans-serif">Standing on Hobbit Eco Village of exquisite beauty in Haputale, Sri Lanka.

Haputale is a small village located among the peaks of Sri Lanka’s central highlands. 
This sleepy village is nestled in a valley peering straight through the Gap to the plain nearly 1000m below and across to the coastline. 
Apart from the mesmerizing natural views, Hobbit Eco Village is surrounded by hilly countryside perfect for walks; through tea plantations, waterfalls, ancient temples and artefacts.
With a handful of shops, hotels and guesthouses, Hobbit Eco Village still remains a remote hamlet. 
This hidden sanctuary with the perfect climate is probably one of the best-kept secrets of the country and is easily accessible from the comfort of our Ella resort in Sri Lanka.</p>
